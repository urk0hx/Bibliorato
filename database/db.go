// GetAllEvidence retrieves all evidence from the database.
func GetAllEvidence() ([]core.Evidence, error) {
	rows, err := db.Query("SELECT id, folder, sender, recipient, date, subject, body_text, body_html, headers FROM evidence")
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	return scanEvidenceRows(rows)
}

// GetEvidenceByFolder retrieves evidence items matching a specific folder name.
func GetEvidenceByFolder(folderName string) ([]core.Evidence, error) {
	rows, err := db.Query("SELECT id, folder, sender, recipient, date, subject, body_text, body_html, headers FROM evidence WHERE folder = ?", folderName)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	return scanEvidenceRows(rows)
}

// GetEvidenceCount returns the total number of evidence items for a given folder (or all if folder is empty).
func GetEvidenceCount(folderName string) (int, error) {
	var count int
	var err error
	if folderName == "" {
		err = db.QueryRow("SELECT COUNT(*) FROM evidence").Scan(&count)
	} else {
		err = db.QueryRow("SELECT COUNT(*) FROM evidence WHERE folder = ?", folderName).Scan(&count)
	}
	return count, err
}

// GetEvidenceByIndex returns a single evidence item at the specified offset (0-based) for a given folder.
func GetEvidenceByIndex(index int, folderName string) (*core.Evidence, error) {
	var query string
	var args []interface{}

	if folderName == "" {
		query = "SELECT id, folder, sender, recipient, date, subject, body_text, body_html, headers FROM evidence ORDER BY id LIMIT 1 OFFSET ?"
		args = []interface{}{index}
	} else {
		query = "SELECT id, folder, sender, recipient, date, subject, body_text, body_html, headers FROM evidence WHERE folder = ? ORDER BY id LIMIT 1 OFFSET ?"
		args = []interface{}{folderName, index}
	}

	row := db.QueryRow(query, args...)
	return scanEvidenceRow(row)
}

func scanEvidenceRow(row *sql.Row) (*core.Evidence, error) {
	var e core.Evidence
	err := row.Scan(&e.ID, &e.Folder, &e.From, &e.To, &e.Date, &e.Subject, &e.BodyText, &e.BodyHTML, &e.Headers)
	if err != nil {
		return nil, err
	}
	return &e, nil
}

func scanEvidenceRows(rows *sql.Rows) ([]core.Evidence, error) {
	var evList []core.Evidence
	for rows.Next() {
		var e core.Evidence
		err := rows.Scan(&e.ID, &e.Folder, &e.From, &e.To, &e.Date, &e.Subject, &e.BodyText, &e.BodyHTML, &e.Headers)
		if err != nil {
			return nil, err
		}
		evList = append(evList, e)
	}
	return evList, nil
}

