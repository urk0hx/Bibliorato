// CloseDB gracefully closes the database connection.
func CloseDB() error {
	if db != nil {
		return db.Close()
	}
	return nil
}

// ClearDB removes all records from all tables to prepare for a new file.
func ClearDB() error {
	if db == nil {
		return fmt.Errorf("database not initialized")
	}

	// Order matters due to foreign keys if we had strict enforcement,
	// but DELETE FROM with no WHERE is efficient.
	tables := []string{"enrichments", "attachments", "evidence"}
	for _, table := range tables {
		_, err := db.Exec(fmt.Sprintf("DELETE FROM %s", table))
		if err != nil {
			return fmt.Errorf("failed to clear table %s: %w", table, err)
		}
		// Reset autoincrement (ignore error if sequence table doesn't exist for this table)
		_, _ = db.Exec("DELETE FROM sqlite_sequence WHERE name=?", table)
	}

	return nil
}
