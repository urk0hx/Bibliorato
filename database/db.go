package database

import (
	"Bibliorato/core"
	"database/sql"
	"fmt"

	_ "modernc.org/sqlite"
)

var db *sql.DB

// InitDB initializes the SQLite database and creates the necessary tables
// for the "Layered Integrity" schema.
func InitDB(filepath string) error {
	var err error
	db, err = sql.Open("sqlite", filepath)
	if err != nil {
		return err
	}

	return createTables()
}

func createTables() error {
	// Layered Integrity Schema: strict separation of pristine evidence vs enrichments.
	evidenceTable := `
	CREATE TABLE IF NOT EXISTS evidence (
		id INTEGER PRIMARY KEY AUTOINCREMENT,
		folder TEXT,
		sender TEXT,
		recipient TEXT,
		date TEXT,
		subject TEXT,
		body_text TEXT,
		body_html TEXT,
		headers TEXT
	);`

	attachmentsTable := `
	CREATE TABLE IF NOT EXISTS attachments (
		id INTEGER PRIMARY KEY AUTOINCREMENT,
		evidence_id INTEGER,
		filename TEXT,
		size INTEGER,
		md5 TEXT,
		sha256 TEXT,
		data BLOB,
		FOREIGN KEY(evidence_id) REFERENCES evidence(id)
	);`

	enrichmentsTable := `
	CREATE TABLE IF NOT EXISTS enrichments (
		id INTEGER PRIMARY KEY AUTOINCREMENT,
		evidence_id INTEGER,
		plugin_name TEXT,
		data TEXT,
		FOREIGN KEY(evidence_id) REFERENCES evidence(id)
	);`

	_, err := db.Exec(evidenceTable)
	if err != nil {
		return fmt.Errorf("error creating evidence table: %v", err)
	}

	_, err = db.Exec(attachmentsTable)
	if err != nil {
		return fmt.Errorf("error creating attachments table: %v", err)
	}

	_, err = db.Exec(enrichmentsTable)
	if err != nil {
		return fmt.Errorf("error creating enrichments table: %v", err)
	}

	return nil
}

// InsertEvidence stores raw, unmodified email metadata.
func InsertEvidence(e *core.Evidence) (int64, error) {
	stmt, err := db.Prepare("INSERT INTO evidence(folder, sender, recipient, date, subject, body_text, body_html, headers) VALUES(?, ?, ?, ?, ?, ?, ?, ?)")
	if err != nil {
		return 0, err
	}
	defer stmt.Close()

	res, err := stmt.Exec(e.Folder, e.From, e.To, e.Date, e.Subject, e.BodyText, e.BodyHTML, e.Headers)
	if err != nil {
		return 0, err
	}

	id, err := res.LastInsertId()
	if err != nil {
		return 0, err
	}

	return id, nil
}

// InsertAttachment stores an attachment in the database.
func InsertAttachment(a *core.Attachment, data []byte) (int64, error) {
	stmt, err := db.Prepare("INSERT INTO attachments(evidence_id, filename, size, md5, sha256, data) VALUES(?, ?, ?, ?, ?, ?)")
	if err != nil {
		return 0, err
	}
	defer stmt.Close()

	res, err := stmt.Exec(a.EvidenceID, a.Filename, a.Size, a.MD5, a.SHA256, data)
	if err != nil {
		return 0, err
	}

	id, err := res.LastInsertId()
	if err != nil {
		return 0, err
	}

	return id, nil
}

// GetAttachmentsByEvidenceID retrieves attachments metadata for a specific evidence item.
func GetAttachmentsByEvidenceID(evidenceID int) ([]core.Attachment, error) {
	rows, err := db.Query("SELECT id, evidence_id, filename, size, md5, sha256 FROM attachments WHERE evidence_id = ?", evidenceID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var attList []core.Attachment
	for rows.Next() {
		var a core.Attachment
		err = rows.Scan(&a.ID, &a.EvidenceID, &a.Filename, &a.Size, &a.MD5, &a.SHA256)
		if err != nil {
			return nil, err
		}
		attList = append(attList, a)
	}
	return attList, nil
}

// GetAttachmentData retrieves the binary data for a specific attachment.
func GetAttachmentData(attachmentID int) ([]byte, error) {
	var data []byte
	err := db.QueryRow("SELECT data FROM attachments WHERE id = ?", attachmentID).Scan(&data)
	if err != nil {
		return nil, err
	}
	return data, nil
}

